#include "libft.h"

int	ft_isalpha(int c)
{
	int	x;
	int	i;

	i = 0;
	x = 0;
	if (c >= 'a' && c <= 'z')
		return (1);
	else if (c >= 'A' && c <= 'Z')
		return (1);
	else
		return (0);
}
